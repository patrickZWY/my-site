const fs = require("node:fs");
const path = require("node:path");

const privateAssetDir = path.join(
  __dirname,
  "..",
  "_site",
  "private-study-assets-v1-621b0c418a9e8c8add0633a3491d19be419716893c1fa7a844a28bf51369ca71"
);
const privateHtmlPath = path.join(privateAssetDir, "rabbithole.html");
const outputPath = path.join(__dirname, "..", "cloudflare", "private-study-page.generated.js");

if (!fs.existsSync(privateHtmlPath)) {
  throw new Error(`Missing generated private study page: ${privateHtmlPath}`);
}

const html = fs.readFileSync(privateHtmlPath, "utf8");

if (!html.includes('id="private-study-title"') || !html.includes("Rabbit Hole")) {
  throw new Error("Generated private study page did not match the expected Rabbit Hole HTML.");
}

const source = [
  "// Generated by scripts/prepare-private-study-worker.js. Do not edit.",
  `export const PRIVATE_STUDY_HTML = ${JSON.stringify(html)};`,
  "",
].join("\n");

fs.writeFileSync(outputPath, source);
fs.rmSync(privateAssetDir, { recursive: true, force: true });
